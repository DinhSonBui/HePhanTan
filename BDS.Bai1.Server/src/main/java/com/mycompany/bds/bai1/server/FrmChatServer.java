    public void run() {
        //Doc du lieu
        try {
            input = new DataInputStream(socket.getInputStream());
            while(true){
                if(socket != null){
                    model.addElement("Client : " + input.readUTF());
                    lsChat.setModel(model);
                }
      
            }
          
        } catch (IOException ex) {
           
        }
    }
